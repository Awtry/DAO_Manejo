    private void Mostrar_Datos() throws SQLException{
        //Traer datos de SQL
        List<Usuario> usuarios = usuarioDao.seleccionar();
        
         for (int i = modelo.getRowCount() - 1; i >= 0; i--) {
            modelo.removeRow(i);
        }
        
        for (int i = 0; i < usuarios.size(); i++) {
            String[] info = new String[5];
            info[0]= String.valueOf(usuarios.get(i).getidUsuario());
            info[1]= usuarios.get(i).getUsuario();
            info[2]= usuarios.get(i).getPassword();
            modelo.addRow(info);
        }
      
    }
    
    private void Agregar_Datos() throws SQLException{

        Usuario insertarUsuario = new Usuario();
        
        insertarUsuario.setUsuario(txtNombre_Usuaio.getText());
        insertarUsuario.setPassword(txtContra_Usuario.getText());

        {
            try {
                usuarioDao.insertar(insertarUsuario);
            } catch (SQLException ex) {
                Logger.getLogger(test_Manejo_Dao_Usuario.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        Mostrar_Datos();
        
        lbl_ID.setText("");
        txtNombre_Usuaio.setText("");
        txtContra_Usuario.setText("");
    }
    private void Actualizar_Datos() throws SQLException{
        
        Usuario actualizarUsuario = new Usuario();
        
        actualizarUsuario.setId_usuario(Integer.parseInt(lbl_ID.getText()));
        actualizarUsuario.setUsuario(txtNombre_Usuaio.getText());
        actualizarUsuario.setPassword(txtContra_Usuario.getText());
        {
            try {
                usuarioDao.actualizar(actualizarUsuario);
            } catch (SQLException ex) {
                Logger.getLogger(test_Manejo_Dao_Usuario.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        
        Mostrar_Datos();
        
        lbl_ID.setText("");
        txtNombre_Usuaio.setText("");
        txtContra_Usuario.setText("");
        
    }
    
    private void Eliminar_Datos() throws SQLException{
        
        Usuario eliminarUsuario = new Usuario();
        eliminarUsuario.setId_usuario(Integer.parseInt(lbl_ID.getText()));
        
        {
            try {
                usuarioDao.eliminar(eliminarUsuario);
            } catch (SQLException ex) {
                Logger.getLogger(test_Manejo_Dao_Usuario.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        
          Mostrar_Datos();
          
        lbl_ID.setText("");
        txtNombre_Usuaio.setText("");
        txtContra_Usuario.setText("");
    }
    
